def area(v1,v2,cm):
    ar = v1*v2
    print(f'A área desse retângulo é: {ar}{cm}')
    

v1 = int(input('Entre com o comprimento do retângulo: '))
v2 = int(input('Entre com a largura do retângulo: '))
cm = input('Centimetros ou metros? [CM/M] ')
area(v1,v2,cm)

